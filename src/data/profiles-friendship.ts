/**
 * 16 MBTI × 4 기질 = 64 조합의 친구 관계 서사 생성
 * MBTI 기본 4문장 + 기질 보강 1문단 = 총 5~6문단
 */
export function generateFriendshipNarrative(mbtiType: string, tempCode: string): string {
  const mbti = mbtiFriendship[mbtiType] ?? mbtiFriendship['ISTJ'];
  const primaryTempBoost = temperamentFriendshipBoost[tempCode[0]] ?? temperamentFriendshipBoost['S'];

  return [
    mbti.circle,
    mbti.makingFriends,
    mbti.maintaining,
    mbti.conflict,
    primaryTempBoost,
  ].join('\n\n');
}
