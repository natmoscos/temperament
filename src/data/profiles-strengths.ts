/**
 * MBTI 강점 5개 + 주기질 강점 보강 2개 = 총 7개 반환
 */
export function generateStrengthsList(mbtiType: string, tempCode: string): string[] {
  const mbti = mbtiStrengthsWeaknesses[mbtiType] ?? mbtiStrengthsWeaknesses['ISTJ'];
  const primaryTemp = temperamentBoosts[tempCode[0]] ?? temperamentBoosts['S'];
  return [...mbti.strengths, ...primaryTemp.strengthBoost];
}

/**
 * MBTI 약점 4개 + 주기질 약점 보강 2개 = 총 6개 반환
 */
export function generateWeaknessBulletsList(mbtiType: string, tempCode: string): string[] {
  const mbti = mbtiStrengthsWeaknesses[mbtiType] ?? mbtiStrengthsWeaknesses['ISTJ'];
  const primaryTemp = temperamentBoosts[tempCode[0]] ?? temperamentBoosts['S'];
  return [...mbti.weaknessBullets, ...primaryTemp.weaknessBoost];
}
