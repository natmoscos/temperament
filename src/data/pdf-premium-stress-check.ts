import type { MbtiType } from './pdf-premium-compatibility';

export interface StressSignal {
  category: 'body' | 'emotion' | 'cognition' | 'behavior' | 'relationship';
  signal: string;
}

export interface StressChecklist {
  intro: string;                     // 유형 맞춤 소개
  signals: StressSignal[];           // 15개 항목 (5 카테고리 × 3개)
  interpretation: {
    low: string;    // 0~4개
    mid: string;    // 5~9개
    high: string;   // 10~15개
  };
  recoveryActions: string[];         // 3~5개 회복 행동
}

const CATEGORY_LABEL: Record<StressSignal['category'], string> = {
  body: '신체',
  emotion: '감정',
  cognition: '인지·사고',
  behavior: '행동',
  relationship: '관계',
};

export function getCategoryLabel(c: StressSignal['category']): string {
  return CATEGORY_LABEL[c];
}

